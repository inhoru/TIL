package ch02.exam08;

public class 논리타입참과거짓BooleanExampl {

	public static void main(String[] args) {
		
		boolean stop = false;
		//스탑이 투르면 if문출력 거짓이면 x
		if (stop) {
			System.out.println("중지합니다.");
			
			//엘스는 if가로안이 참 이니라 거짓이면 실행해라 라는 뜻
		} else {
			System.out.println("시작합니다.");
		}
		
		

	}

}

